#include "../../incs/minishell.h"

int	cond_fs_1(char *str, t_nk *c)
{
	return ((str[c->m] == '$' && c->i != 2 && c->j != 1)
		&& !(c->m > 0 && str[c->m - 1] == 34
			&& str[c->m + 1] && str[c->m + 1] == 34));
}
